const normalizeListeningAnswer = (value) =>
  String(value || "")
    .replace(/\s+/g, "")
    .replace(/[−－]/g, "-")
    .replace(/[。．.、,，!！?？]/g, "")
    .trim();

const listeningTypes = [...new Set(audioSamples.map((sample) => sample.type))];

const listeningTypeLabels = Object.fromEntries(
  listeningTypes.map((type) => [type, type])
);

const getListeningSamples = (type = listeningTypes[0]) => {
  const selectedType = listeningTypes.includes(type) ? type : listeningTypes[0];
  return audioSamples.filter((sample) => sample.type === selectedType);
};

const getListeningAnswerValues = (sample) =>
  [...new Set([sample?.v, sample?.p].filter(Boolean))];

